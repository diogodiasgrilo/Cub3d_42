#include "libft.h"

int	ft_isalnum(char c)
{
	unsigned char	c1;

	c1 = c;
	if ((c1 < 65 || c1 > 90) && (c1 < 97 || c1 > 122) && (c1 < 48 || c1 > 57))
		return (0);
	return (1);
}
